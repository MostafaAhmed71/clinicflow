/** Built-in templates for a clinic specialty (+ common pack) */
export function getBuiltinInstructionTemplates(
  specialty: string | null | undefined,
): BuiltinInstructionTemplate[] {
  const pack = getSpecialtyPack(specialty)
  const specific = BY_SPECIALTY[pack.id] ?? BY_SPECIALTY.general
  return [...COMMON_INSTRUCTION_TEMPLATES, ...specific]
}

export function listSpecialtyInstructionPacks(): {
  specialty: ClinicSpecialty
  templates: BuiltinInstructionTemplate[]
}[] {
  return (Object.keys(BY_SPECIALTY) as ClinicSpecialty[]).map((specialty) => ({
    specialty,
    templates: BY_SPECIALTY[specialty],
  }))
}

export function isBuiltinInstructionId(id: string) {
  return id.startsWith('builtin:')
}
