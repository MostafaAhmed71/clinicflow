export type BuiltinInstructionTemplate = {
  /** Stable id: builtin:{specialty}:{slug} */
  id: string
  specialty: ClinicSpecialty | 'common'
  name: string
  body: string
}

function tpl(
  specialty: ClinicSpecialty | 'common',
  slug: string,
  name: string,
  lines: string[],
): BuiltinInstructionTemplate {
  return {
    id: `builtin:${specialty}:${slug}`,
    specialty,
    name,
    body: lines.map((l) => l.trim()).filter(Boolean).join('\n'),
  }
}

